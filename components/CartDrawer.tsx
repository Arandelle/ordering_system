"use client";

import React from "react";
import { useCart } from "@/contexts/CartContext";
import { ArrowRight, Minus, Plus, ShoppingBag, Trash2, X } from "lucide-react";
import { useRouter } from "next/navigation";

const CartDrawer = () => {
  const router = useRouter();

  const {
    cartItems,
    isCartOpen,
    setIsCartOpen,
    updateQuantity,
    removeFromCart,
    totalPrice,
    clearCart,
  } = useCart();

  const handleCheckout = () => {
    setIsCartOpen(false);
    router.push("/checkout");
  };

  if (!isCartOpen) return null;

   const scrollToMenu = () => {
    router.push("/menu");
    setIsCartOpen(false);;
  };

  return (
    <>
      {/** Backdrop */}
      <div
        className="fixed inset-0 bg-black/50 z-45 transition-opacity"
        onClick={() => setIsCartOpen(false)}
      />
      {/** Drawer */}
      <div className="fixed right-0 top-0 h-full w-full max-w-md bg-white z-60 shadow-2xl transition-transform duration-300 flex flex-col">
        {/** Header */}
        <div className="flex items-center justify-between p-6 border-b border-gray-100">
          <div className="flex items-center gap-3">
            <ShoppingBag className="text-[#e13e00]" size={24} />
            <h2 className="text-xl font-bold text-gray-900">Your order</h2>

            {cartItems.length > 0 && (
              <span className="bg-[#e13e00] text-white text-xs font-bold px-2 py-0.5 rounded-full">
                {cartItems.reduce((sum, item) => sum + item.quantity, 0)}
              </span>
            )}
          </div>
          <button
            onClick={() => setIsCartOpen(false)}
            className="p-2 hover:bg-gray-100 rounded-full transition-colors"
          >
            <X size={24} className="text-gray-500" />
          </button>
        </div>

        {/** Cart Items */}
        <div className="flex-1 overflow-y-auto p-6">
          {cartItems.length === 0 ? (
            <div className="flex flex-col items-center justify-center h-full text-center space-y-2">
              <div className="p-2 bg-gray-100 rounded-full items-center">
                <ShoppingBag size={24} className="text-slate-600" />
              </div>
              <h3 className="text-lg font-semibold text-gray-400">
                Your cart is empty
              </h3>
              <p className="text-gray-400 text-sm">
                Add some delicious items to get started!
              </p>
              <button 
              onClick={scrollToMenu}
              className="px-4 py-2 mt-4 rounded-xl bg-[#e13e00] hover:bg-[#c13500] text-white transition-colors shadow-xl cursor-pointer">
                Order Now!
              </button>
            </div>
          ) : (
            <div className="space-y-4">
              {cartItems.map((item) => (
                <div
                  key={item.id}
                  className="flex gap-4 bg-gray-50 rounded-xl p-4"
                >
                  <img
                    src={item.image}
                    alt={item.name || "product image"}
                    className="w-20 h-20 object-cover rounded-lg"
                  />
                  <div className="flex-1">
                    <h4 className="font-semibold text-gray-900 mb-1">
                      {item.name}
                    </h4>
                    <p className="text-[#e13e00] font-bold">₱{item.price}</p>

                    <div className="flex items-center justify-between mt-2">
                      <div className="flex items-center gap-2 bg-white rounded-full border border-gray-200">
                        <button
                          onClick={() =>
                            updateQuantity(item.id, item.quantity - 1)
                          }
                          className="p-1.5 hover:bg-gray-100 rounded-full transition-colors cursor-pointer"
                        >
                          <Minus size={14} />
                        </button>
                        <span className="font-semibold text-sm w-6 text-center">
                          {item.quantity}
                        </span>
                        <button
                          onClick={() =>
                            updateQuantity(item.id, item.quantity + 1)
                          }
                          className="p-1.5 hover:bg-gray-100 rounded-full transition-colors cursor-pointer"
                        >
                          <Plus size={14} />
                        </button>
                      </div>

                      <button
                        onClick={() => removeFromCart(item.id)}
                        className="p-2 text-red-500 hover:bg-red-50 rounded-full transition-colors cursor-pointer"
                      >
                        <Trash2 size={16} />
                      </button>
                    </div>
                  </div>
                </div>
              ))}
            </div>
          )}
        </div>

        {/** Footer */}
        {cartItems.length > 0 && (
          <div className="border-t border-gray-100 p-6 space-y-4">
            <div className="space-y-2">
              <div className="flex items-center justify-between text-sm text-gray-500">
                <span>Subtotal</span>
                <span>₱{totalPrice.toFixed(2)}</span>
              </div>
              <div className="flex items-center justify-between text-sm text-gray-500">
                <span>Deliver Fee</span>
                <span>{totalPrice >= 500 ? "Free" : "₱50.00"}</span>
              </div>
              <div className="flex items-center justify-between pt-2 border-t border-gray-100">
                <span className="font-semibold text-gray-900">Total</span>
                <span className="font-bold text-xl text-[#e13e00]">
                  ₱{(totalPrice + (totalPrice >= 500 ? 0 : 50)).toFixed(2)}
                </span>
              </div>
            </div>
            <button
              onClick={handleCheckout}
              className="w-full bg-[#e13e00] hover:bg-[#c13500] text-white py-4 rounded-xl font-bold text-lg transition-colors shadow-lg hover:shadow-xl flex items-center justify-center gap-2 cursor-pointer"
            >
              Proceed to checkout
              <ArrowRight size={20} />
            </button>
            <button
              onClick={clearCart}
              className="w-full text-gray-500 hover:text-red-500 py-2 text-sm transition-colors cursor-pointer"
            >
              Clear Cart
            </button>
          </div>
        )}
      </div>
    </>
  );
};

export default CartDrawer;
